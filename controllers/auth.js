exports.postLogin = (req, res, next) => {
  const [email, password] = [req.body.email, req.body.password];
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    return res.status(422).render("auth/login", {
      path: "/login",
      pageTitle: "Login",
      errorMessage: errors.array()[0].msg,
      oldInput: {
        email: email,
        password: password,
      },
      validationErrors: errors.array(),
    });
  }

  User.findOne({ email: email })
    .then((user) => {
      if (!user) {
        // use flash to display error
        // req.flash('error', 'Invalid email/password.');
        // non existing user, redirect to login
        return res.status(422).render("auth/login", {
          path: "/login",
          pageTitle: "Login",
          errorMessage: "Invalid email/password combination.",
          oldInput: {
            email: email,
            password: password,
          },
          validationErrors: [],
        });
      }
      bcrypt
        .compare(password, user.password)
        .then((doMatch) => {
          // if the returned value is true, then password is match
          if (doMatch) {
            req.session.isLoggedIn = true;
            req.session.user = user;
            return req.session.save((err) => {
              if (err) {
                console.log(err);
              }
              res.redirect("/");
            });
          }
          return res.status(422).render("auth/login", {
            path: "/login",
            pageTitle: "Login",
            errorMessage: "Invalid email/password combination.",
            oldInput: {
              email: email,
              password: password,
            },
            validationErrors: [],
          });
        })
        .catch((err) => {
          console.log(err);
          res.redirect("/login");
        });
    })
    .catch((err) => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    });
};

exports.postSignup = (req, res, next) => {
  const email = req.body.email;
  const password = req.body.password;
  // validationResult will express-validator middleware (via check on routes) will collect errors
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    console.log(errors.array());
    // 422 is a common status code for failed validation
    return res.status(422).render("auth/signup", {
      path: "/signup",
      pageTitle: "Signup",
      errorMessage: errors.array()[0].msg,
      oldInput: {
        email: email,
        password: password,
        confirmPassword: req.body.confirmPassword,
      },
      validationErrors: errors.array(),
    });
  }
  return bcrypt
    .hash(password, 12)
    .then((hashedPassword) => {
      // no user found for email create new user
      const user = new User({
        email: email,
        password: hashedPassword,
        cart: { items: [] },
      });
      // return the user.save() to chain then block
      return user.save();
    })
    .then((result) => {
      // redirect to login after signup to login
      res.redirect("/login");
      return transporter.sendMail({
        to: email,
        from: "shop@shibestore.com",
        subject: "Wow Signup Success",
        html:
          "<h1>Welcome to the Shibe store. Prepare to have your wallet drained.</h1>",
      });
    })
    .catch((err) => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    });
};

